    public ArrayList<String> getListaAleatoria(int qtde) 
    { 
        Random random = new Random();
        ArrayList<String> listaAleatoria = new ArrayList<>(); 
        for (int i = 0; i < qtde; i++) { 
  
            // Pega um índice aleatório entre 0 e o tamanho da lista
            int indiceAleatorio = random.nextInt(listaDeNomes.size()); 
            // adiciona o elemento à listaAleatoria
            listaAleatoria.add(listaDeNomes.get(indiceAleatorio)); 
            // Remove o elemento do índice para evitar repetições
            listaDeNomes.remove(indiceAleatorio); 
        } 
        return listaAleatoria; 
    } 
